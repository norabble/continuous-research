const INTERPRETATION_WORKFLOW = `---
description: "Interpretation step: write the impact declaration for a new data edition onto its data-PR"
on:
  pull_request:
    types: [opened, reopened]
  bots:
    # TODO: your GitHub App's slug — the author of the engine's data-PRs.
    - your-app-slug
engine:
  # Engine/model are per-instance choices. This pairing is proven against
  # free-tier quotas (~16–25 API requests per session; smaller "lite"-class
  # models below gemini-3.1-flash-lite have failed the harness protocol).
  id: gemini
  model: gemini-3.1-flash-lite
timeout-minutes: 15
permissions:
  contents: read
  pull-requests: read
safe-outputs:
  push-to-pull-request-branch:
    # The interpretation contract: the agent may touch ONLY the impact
    # declaration and the living findings doc. allowed-files is the binding
    # constraint; protected-files must be 'allowed' because .research/ is a
    # top-level dot-folder, which the default 'blocked' policy rejects even
    # for allowlisted paths.
    protected-files: allowed
    allowed-files:
      - ".research/impact/*.md"
      - "findings.md"
---

# Interpretation step

You are the **interpretation step** of a Continuous Research instance. A
data-PR proposing a new data edition has just been opened. Your job is to
write the **impact declaration**: what the new edition means for the existing
claims.

## Guard

First check the triggering pull request's labels. If it has no label starting
with \`data:\`, stop immediately and do nothing. Otherwise, the part after
\`data:\` is the **descriptor**.

## Read

Check out the PR's branch. Read:

1. TODO: the new edition's artifact(s) for this project — describe where the
   sensor/pipeline writes them, e.g. \`data/<source>/<descriptor>.json\`.
2. \`findings.md\` — the living prose. Each claim paragraph is followed by an
   invisible HTML-comment annotation. Its inner content has this form (the
   line in the file is wrapped as an HTML comment):

   \`\`\`text
   claim: <id> | backs: <result keys> | status: <status>
   \`\`\`

## Write

On the PR branch, make exactly two changes:

1. **Create \`.research/impact/<descriptor>.md\`** — the impact declaration:
   - **Prior claim** — quote the current claim and its status.
   - **What changed** — the new edition's values vs. the ones in the prose.
   - **Assessment** — one of **strengthened / weakened / overturned**, with a
     short justification. Distinguish one-off moves from real changes; do not
     overclaim.
   - **Revised claim** — the new claim text.
2. **Update \`findings.md\`** — replace the claim paragraph with the revised
   claim (keep the same claim id in the annotation; update the values, the
   edition reference, and the \`status\` field as assessed).

Keep the tone factual and appropriately hedged. Commit both files to the PR
branch with the message \`interpretation(<descriptor>): impact declaration\`.
`;

