/**
 * `continuous-research init` scaffolding (Phase-1 plan, design rule 3: the CLI
 * only scaffolds; credential / identity / App setup stays documented manual
 * steps). Pure — returns the files to write; the I/O wrapper in `cli.ts`
 * writes them, skipping any that already exist.
 *
 * The templates mirror the configuration PROVEN in continuous-research-sample
 * (2026-07-02): data-PRs are opened under a GitHub App identity (default-token
 * PRs cannot trigger the gh-aw interpretation workflow), and interpretation is
 * a gh-aw agentic workflow the instance compiles with `gh aw compile`.
 *
 * `\${{ ... }}` in the templates is escaped so it survives as a literal GitHub
 * Actions expression, not a TS interpolation.
 */

export interface ScaffoldFile {
  path: string;
  content: string;
}

