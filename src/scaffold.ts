/**
 * `continuous-research init` scaffolding (Phase-1 plan, design rule 3: the CLI
 * only scaffolds `.research/` + the workflows; credential / identity / App
 * setup stays documented manual steps). Pure — returns the files to write; the
 * I/O wrapper in `cli.ts` writes them, skipping any that already exist.
 *
 * `\${{ ... }}` in the templates is escaped so it survives as a literal GitHub
 * Actions expression, not a TS interpolation.
 */

export interface ScaffoldFile {
  path: string;
  content: string;
}

const CONFIG = `{
  "sensor": "node sensor.mjs"
}
`;

const SENSE_WORKFLOW = `name: sense

# Generated by \`continuous-research init\`.
on:
  workflow_dispatch:
  # Add a schedule once your sensor is wired, e.g.:
  # schedule:
  #   - cron: "0 6 * * *"

# Serialize runs so two can't both see "new" and double-propose.
concurrency:
  group: sense
  cancel-in-progress: false

permissions:
  contents: write
  pull-requests: write
  issues: write

jobs:
  sense:
    runs-on: ubuntu-latest
    steps:
      - uses: actions/checkout@v4
      - uses: actions/setup-node@v4
        with:
          node-version: "22"
      # Public path: \`npx github:norabble/continuous-research\`. While the
      # framework is private, vendor the engine and use instead:
      #   run: node engine/continuous-research.mjs sense
      - name: sense
        env:
          GITHUB_TOKEN: \${{ secrets.GITHUB_TOKEN }}
        run: npx --yes github:norabble/continuous-research sense
`;

const DECLINE_WORKFLOW = `name: decline

on:
  pull_request:
    types: [closed]

permissions:
  contents: write
  issues: read
  pull-requests: read

jobs:
  record-decline:
    # Only closed-unmerged data-PRs (the engine also no-ops defensively).
    if: >-
      github.event.pull_request.merged == false &&
      contains(join(github.event.pull_request.labels.*.name, ','), 'data:')
    runs-on: ubuntu-latest
    steps:
      - uses: actions/checkout@v4
      - uses: actions/setup-node@v4
        with:
          node-version: "22"
      - name: record-decline
        env:
          GITHUB_TOKEN: \${{ secrets.GITHUB_TOKEN }}
        run: npx --yes github:norabble/continuous-research record-decline
`;

export const NEXT_STEPS = `
Next steps:
  1. Provide your sensor — a command that prints a detection-result JSON on
     stdout — and point .research/config.json "sensor" at it.
  2. Wire auth for the workflows: the public "npx github:" path needs the
     framework public; otherwise vendor the engine or use a PAT (see docs).
  3. Commit .research/ and .github/workflows/, then dispatch the "sense" workflow.
`;

export function scaffoldFiles(): ScaffoldFile[] {
  return [
    { path: ".research/config.json", content: CONFIG },
    { path: ".github/workflows/sense.yml", content: SENSE_WORKFLOW },
    { path: ".github/workflows/decline.yml", content: DECLINE_WORKFLOW },
  ];
}
